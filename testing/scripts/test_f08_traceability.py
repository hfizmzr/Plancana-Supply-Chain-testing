# ─────────────────────────────────────────────────────────────
#  BATCH IDs — loaded automatically from batch_ids.json
#  (generated by:  node seed_test_data.js)
#
#  If the file is missing, fall back to the hardcoded values
#  below and update them manually.
# ─────────────────────────────────────────────────────────────
def _load_batch_ids():
    json_path = os.path.join(os.path.dirname(__file__), "batch_ids.json")
    if os.path.exists(json_path):
        with open(json_path) as f:
            data = json.load(f)
        valid      = data.get("VALID_BATCH_ID",      "REPLACE_WITH_VALID_BATCH_ID")
        incomplete = data.get("INCOMPLETE_BATCH_ID",  "REPLACE_WITH_INCOMPLETE_BATCH_ID")
        print(f"\n[seed] Loaded batch IDs from batch_ids.json")
        print(f"       VALID_BATCH_ID:      {valid}")
        print(f"       INCOMPLETE_BATCH_ID: {incomplete}")
        return valid, incomplete
    return "REPLACE_WITH_VALID_BATCH_ID", "REPLACE_WITH_INCOMPLETE_BATCH_ID"


VALID_BATCH_ID, INCOMPLETE_BATCH_ID = _load_batch_ids()

WAIT = 15
# ─────────────────────────────────────────────────────────────


def _url(batch_id: str) -> str:
    return f"{BASE_URL}/verify/{urllib.parse.quote(str(batch_id), safe='')}"


def _wait_load(driver, timeout=WAIT):
    """Block until the loading spinner disappears."""
    try:
        WebDriverWait(driver, timeout).until(
            EC.invisibility_of_element_located((By.CSS_SELECTOR, ".animate-spin"))
        )
    except TimeoutException:
        pass


def _find(driver, xpath, timeout=WAIT):
    return WebDriverWait(driver, timeout).until(
        EC.presence_of_element_located((By.XPATH, xpath))
    )


