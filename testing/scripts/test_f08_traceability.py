class TestTC08005:
    """
    Pre-conditions : Logged in as Retailer; navigated to the traceability page.
    Post-conditions: Core UI elements present and functional.

    Note: The app uses URL navigation rather than a form-based batch ID entry.
    TCON-08-008 → batch ID visible in page body.
    TCON-08-009 → Supply Chain Journey section rendered and interactive.
    """

    @pytest.fixture(autouse=True)
    def setup(self, driver):
        self.d = driver

    def test_page_loads_successfully(self):
        """Page loads and a top-level heading is visible."""
        login_as_retailer(self.d)
        self.d.get(_url(VALID_BATCH_ID))
        _wait_load(self.d)

        heading = WebDriverWait(self.d, WAIT).until(
            EC.presence_of_element_located((By.CSS_SELECTOR, "h1"))
        )
        assert heading.is_displayed(), "Page did not load — no h1 heading found"

    def test_batch_id_visible_on_page(self):
        """TCON-08-008: Batch ID is shown on the page (breadcrumb or heading).

        Implementation note: JSX renders 'Batch #{batchId}' as two separate DOM text
        nodes ('Batch #' + the ID value).  XPath text() only matches the FIRST text
        node, so contains(text(), BATCH_ID) silently fails.  We search page_source
        instead, which is reliable for any rendering pattern.
        """
        login_as_retailer(self.d)
        self.d.get(_url(VALID_BATCH_ID))
        _wait_load(self.d)

        assert VALID_BATCH_ID in self.d.page_source, (
            f"Batch ID '{VALID_BATCH_ID}' is not present anywhere on the page"
        )

    def test_supply_chain_section_rendered_and_interactive(self):
        """TCON-08-009: Supply Chain Journey section renders; toggle buttons are clickable."""
        login_as_retailer(self.d)
        self.d.get(_url(VALID_BATCH_ID))
        _wait_load(self.d)

        section = _find(self.d, "//*[contains(text(),'Supply Chain Journey')]")
        assert section.is_displayed(), "Supply Chain Journey section is not rendered"

        toggles = self.d.find_elements(
            By.XPATH,
            "//button[contains(text(),'Flowchart') or contains(text(),'List View')]",
        )
        assert len(toggles) >= 1, "View-toggle buttons not found in Supply Chain section"
        toggles[0].click()


# ═════════════════════════════════════════════════════════════
#  TC-08-006: Error Guessing — Security and Edge Cases
#  TCOV: 08-020, 08-021, 08-022, 08-023, 08-024
# ═════════════════════════════════════════════════════════════
class TestTC08006:
    """
    Pre-conditions : User is on the traceability page.
    Post-conditions: No crash, no data leakage, no script execution.
    """

    @pytest.fixture(autouse=True)
    def setup(self, driver):
        self.d = driver

    def test_whitespace_only_batch_id(self):
        """TCON-08-020: Whitespace-only batch ID — no timeline rendered."""
        self.d.get(_url("   "))
        _wait_load(self.d)

        timeline = self.d.find_elements(By.XPATH, "//*[contains(text(),'Supply Chain Journey')]")
        assert len(timeline) == 0, "Timeline must NOT appear for whitespace-only batch ID"

    def test_sql_injection_no_data_exposed(self):
        """TCON-08-021: SQL injection pattern — no data returned, no crash."""
        self.d.get(_url("' OR '1'='1"))
        _wait_load(self.d)

        assert self.d.title is not None, "Page crashed on SQL injection input"

        timeline = self.d.find_elements(By.XPATH, "//*[contains(text(),'Supply Chain Journey')]")
        assert len(timeline) == 0, "No data should be returned for SQL injection batch ID"

    def test_xss_script_not_executed(self):
        """TCON-08-022: XSS <script> tag in batch ID — alert must NOT fire."""
        self.d.get(_url("<script>alert('xss')</script>"))
        _wait_load(self.d)

        try:
            alert = self.d.switch_to.alert
            alert_text = alert.text
            alert.dismiss()
            pytest.fail(f"XSS executed — alert appeared with: '{alert_text}'")
        except Exception:
            pass  # No alert = XSS correctly blocked

        assert self.d.title is not None, "Page crashed on XSS payload"

    def test_excessively_long_batch_id(self):
        """TCON-08-023: 500-character batch ID — no crash, no timeline."""
        self.d.get(_url("A" * 500))
        _wait_load(self.d)

        assert self.d.title is not None, "Page crashed on a 500-character batch ID"
        assert "500" not in self.d.title, "Server 500 error on long batch ID"

        timeline = self.d.find_elements(By.XPATH, "//*[contains(text(),'Supply Chain Journey')]")
        assert len(timeline) == 0, "No data should be returned for an excessively long batch ID"

    def test_rapid_repeated_navigation(self):
        """TCON-08-024: Navigate to the same verify URL 5 times rapidly — no crash."""
        url = _url(VALID_BATCH_ID)

        for _ in range(5):
            self.d.get(url)
            time.sleep(0.3)

        _wait_load(self.d)

        assert self.d.title is not None, "Page became unstable after rapid repeated navigation"
        assert "500" not in self.d.title, "Server error after rapid repeated navigation"
